<template>
  <el-card>
    <el-table border style="margin: 10px 0" :data="skuArr">
      <el-table-column
        label="序号"
        type="index"
        align="center"
        width="80px"
      ></el-table-column>
      <el-table-column
        label="名称"
        show-overflow-tooltip
        prop="skuName"
      ></el-table-column>
      <el-table-column
        label="描述"
        show-overflow-tooltip
        prop="skuDesc"
      ></el-table-column>
      <el-table-column label="默认图片">
        <template #default="{ row }">
          <el-image
            :src="row.skuDefaultImg"
            :alt="row.skuName"
            style="width: 100px; height: 100px; cursor: pointer"
            fit="cover"
          />
        </template>
      </el-table-column>
      <el-table-column label="重量" prop="weight"></el-table-column>
      <el-table-column label="价格" prop="price"></el-table-column>
      <el-table-column label="操作" width="250px" fixed="right">
        <template #default="{ row }">
          <el-button
            size="small"
            type="primary"
            :icon="row.isSale == 1 ? 'Bottom' : 'Top'"
            @click="updateSale(row)"
            :title="row.isSale == 1 ? '下架' : '上架'"
          ></el-button>
          <el-button
            size="small"
            type="primary"
            icon="Edit"
            @click="updateSku"
          ></el-button>
          <el-button
            size="small"
            type="primary"
            icon="InfoFilled"
            title="查看"
            @click="findSku(row)"
          ></el-button>
          <el-popconfirm
            :title="`你确定要删除${row.skuName}?`"
            width="200px"
            @confirm="removeSku(row.id)"
          >
            <template #reference>
              <el-button
                size="small"
                type="primary"
                icon="Delete"
                title="删除"
              ></el-button>
            </template>
          </el-popconfirm>
        </template>
      </el-table-column>
    </el-table>
    <el-pagination
      v-model:current-page="pageNo"
      v-model:page-size="pageSize"
      :page-sizes="[10, 20, 30, 40]"
      :background="true"
      layout="prev,pager,next,jumper,->,sizes,total"
      :total="total"
      @current-change="getHasSku"
      @size-change="handler"
    />
    <!-- 抽屉组件：展示商品详情 -->
    <el-drawer v-model="drawer">
      <!-- 标题部分 -->
      <template #header>
        <h2 style="color: black; font-weight: bold">查看商品详情</h2>
      </template>
      <template #default>
        <el-row style="margin: 10px 0">
          <el-col :span="6">名称</el-col>
          <el-col :span="18">{{ skuInfo.skuName }}</el-col>
        </el-row>
        <el-row style="margin: 10px 0">
          <el-col :span="6">描述</el-col>
          <el-col :span="18">{{ skuInfo.skuDesc }}</el-col>
        </el-row>
        <el-row style="margin: 10px 0">
          <el-col :span="6">价格</el-col>
          <el-col :span="18">{{ skuInfo.price }}</el-col>
        </el-row>
        <el-row style="margin: 20px 0">
          <el-col :span="6">平台属性</el-col>
          <el-col :span="18">
            <el-tag
              style="margin: 5px"
              v-for="item in skuInfo.skuAttrValueList"
              :key="item.attrId"
              >{{ item.valueName }}</el-tag
            >
          </el-col>
        </el-row>
        <el-row style="margin: 20px 0">
          <el-col :span="6">销售属性</el-col>
          <el-col :span="18">
            <el-tag
              style="margin: 5px"
              type="info"
              v-for="item in skuInfo.skuSaleAttrValueList"
              :key="item.id"
              >{{ item.saleAttrValueName }}</el-tag
            >
          </el-col>
        </el-row>
        <el-row style="margin: 20px 0">
          <el-col :span="6">商品图片</el-col>
          <el-col :span="18">
            <el-carousel :interval="4000" type="card" height="200px">
              <el-carousel-item
                v-for="item in skuInfo.skuImageList"
                :key="item.id"
              >
                <img
                  :src="item.imgUrl"
                  :alt="skuInfo.skuName"
                  style="width: 100%; height: 100%"
                />
              </el-carousel-item>
            </el-carousel>
          </el-col>
        </el-row>
      </template>
    </el-drawer>
  </el-card>
</template>

<script setup lang="ts">
import { ref, onMounted } from "vue";
//引入请求
import {
  reqSkuList,
  reqSaleSku,
  reqCancelSale,
  reqSkuInfo,
  reqRemoveSku,
} from "@/api/product/sku";
import type {
  SkuResponseData,
  SkuData,
  SkuInfoData,
} from "@/api/product/sku/type";
import { ElMessage } from "element-plus";
//分页器当前页面
let pageNo = ref<number>(1);
//每一页展示几条数据
let pageSize = ref<number>(10);
let total = ref<number>(0);
let skuArr = ref<SkuData[]>([]);
//控制抽屉显示与隐藏
let drawer = ref<boolean>(false);
let skuInfo = ref<SkuData>({});
//组件挂载完毕
onMounted(() => {
  getHasSku();
});

const getHasSku = async (pager: number = 1) => {
  //当前分页器的页面。默认第一页
  pageNo.value = pager;
  let result: SkuResponseData = await reqSkuList(pageNo.value, pageSize.value);
  if (result.code == 200) {
    total.value = result.data.total;
    skuArr.value = result.data.records;
  }
};
//分页器下拉菜单发送变化时候触发
const handler = () => {
  getHasSku();
};
//商品上架与下架的操作
const updateSale = async (row: SkuData) => {
  //row.isSale==1 说明商品处于上架状态,更新为下架阶段
  if (row.isSale == 1) {
    //下架操作
    let result: any = await reqCancelSale(row.id);
    if (result.code == 200) {
      //提示信息
      ElMessage({ type: "success", message: "下架成功" });
      //发请求再次获取sku数据
      getHasSku(pageNo.value);
    }
  } else {
    //上架操作
    let result = await reqSaleSku(row.id);
    if (result.code == 200) {
      ElMessage({ type: "success", message: "上架成功" });
      getHasSku(pageNo.value);
    }
  }
};
//更新已有的sku
const updateSku = () => {
  ElMessage({ type: "success", message: "程序员正在努力更新中..." });
};
//查看商品详情sku的回调
const findSku = async (row: SkuData) => {
  drawer.value = true;
  //发请求获取已有的商品详情数据
  let result: SkuInfoData = await reqSkuInfo(row.id);
  //存储已有的sku
  skuInfo.value = result.data;
};
//删除某一个已有的商品
const removeSku = async (id: number) => {
  let result: any = await reqRemoveSku(id);
  if (result.code == 200) {
    ElMessage({ type: "success", message: "删除成功" });
    getHasSku(skuArr.value.length > 1 ? pageNo.value : pageNo.value - 1);
  } else {
    ElMessage({ type: "error", message: "系统数据不能删除" });
  }
};
</script>
<script lang="ts">
export default {
  name: "Sku",
};
</script>

